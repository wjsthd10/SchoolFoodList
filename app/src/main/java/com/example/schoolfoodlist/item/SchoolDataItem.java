package com.example.schoolfoodlist.item;

public class SchoolDataItem {
    String sName;
    String sCode;
    String atptCode;
    String address;

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public String getsCode() {
        return sCode;
    }

    public void setsCode(String sCode) {
        this.sCode = sCode;
    }

    public String getAtptCode() {
        return atptCode;
    }

    public void setAtptCode(String atptCode) {
        this.atptCode = atptCode;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
